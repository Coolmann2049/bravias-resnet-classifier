"""
data_loader.py
==============
SimXRD-4M Dataset Loader for DeepBravais.

Downloads powder X-ray diffraction (PXRD) patterns directly from the
SimXRD-4M dataset (hosted on Hugging Face / GitHub by Bin Cao et al.),
maps the 230 space groups to the 14 Bravais lattices, performs stratified
sampling to produce a balanced dataset, and saves it as a compressed .npz
or HDF5 file — ready to be dropped into the same training pipeline as
data generated by data_generator.py.

Target runtime: Kaggle (16 GB RAM, 12-hour session). All design decisions
(streaming, chunked I/O, memory guards) are made with that budget in mind.

References
----------
- Cao, B. et al. (2024). SimXRD-4M: Big Simulated X-ray Diffraction Data
  Accelerates the Crystalline Symmetry Classification. ICLR 2025.
  https://arxiv.org/abs/2406.15469
- Dataset organisation (Hugging Face): https://huggingface.co/AI4Spectro
  - Training Part 1 : AI4Spectro/ILtrainV1_P1  (74.1 GB, 5 split-gz parts)
  - Training Part 2 : AI4Spectro/ILtrainV1_P2
  - Validation      : AI4Spectro/ILvalV1
  - Test            : AI4Spectro/ILtestV1
- Original GitHub   : https://github.com/Bin-Cao/SimXRD
- NOTE: The old caobin/SimXRD Hugging Face URL is now 404. Use AI4Spectro.

⚠️  Kaggle disk warning
-----------------------
Each training part is ~74 GB compressed. Kaggle notebooks only have ~20 GB
disk space, so full download is NOT possible on Kaggle.

For Kaggle, use data_generator.py (pymatgen) with --n_samples 500000 instead.
For a server with 200+ GB disk, download ILtrainV1_P1 and ILtrainV1_P2,
combine the 5 split-gz parts, gunzip, then point --db_dir at the .db file.

Setup
-----
Install required packages BEFORE running this script:

    pip install ase tqdm numpy h5py huggingface_hub requests

The script downloads the SimXRD .db files via the Hugging Face Hub API
one shard at a time. You do NOT need to download 4 million rows upfront.

Usage
-----
    # Default: 5 000 samples per class → 70 000 total, saved as .npz
    python data_loader.py

    # Custom subset
    python data_loader.py --n_per_class 3000 --format h5 \
                          --output data/processed/simxrd_balanced.h5

    # Resume from already-downloaded .db shard files (no re-download)
    python data_loader.py --db_dir /kaggle/input/simxrd-shards --no_download
"""

