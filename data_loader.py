HF_REPO_ID      = "AI4Spectro/ILtrainV1_P1"   # Part 1 of training data
HF_REPO_ID_P2   = "AI4Spectro/ILtrainV1_P2"   # Part 2 of training data
HF_REPO_TYPE    = "dataset"


def _list_db_files(repo_id: str = HF_REPO_ID) -> List[str]:
    """
    List all .db shard files in the SimXRD Hugging Face repository.
    Returns relative paths as they appear in the repo.
    """
    if not HF_HUB_AVAILABLE:
        raise RuntimeError("huggingface_hub not installed. "
                           "Run: pip install huggingface_hub")
    all_files = list(list_repo_files(repo_id, repo_type=HF_REPO_TYPE))
    db_files  = sorted(f for f in all_files if f.endswith(".db"))
    return db_files


def _download_shard(
    repo_file: str,
    local_dir: str,
    repo_id: str = HF_REPO_ID,
) -> str:
    """
    Download a single .db shard from the Hugging Face Hub.
    Returns the local path to the downloaded file.
    Uses the Hub cache so repeated calls do NOT re-download.
    """
    if not HF_HUB_AVAILABLE:
        raise RuntimeError("huggingface_hub not installed.")

    local_path = hf_hub_download(
        repo_id   = repo_id,
        filename  = repo_file,
        repo_type = HF_REPO_TYPE,
        local_dir = local_dir,
    )
    return local_path


