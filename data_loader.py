def save_npz(X: np.ndarray, y: np.ndarray, output_path: str) -> None:
    """Save dataset as a compressed NumPy .npz file."""
    os.makedirs(os.path.dirname(os.path.abspath(output_path)), exist_ok=True)
    np.savez_compressed(
        output_path,
        X            = X,
        y            = y,
        class_names  = np.array(CLASS_NAMES),
        Q_axis       = Q_AXIS,
        source       = np.array("SimXRD-4M (caobin/SimXRD, Hugging Face)"),
    )
    size_mb = os.path.getsize(output_path + ".npz") / 1e6
    print(f"[data_loader] Saved → {output_path}.npz  "
          f"({size_mb:.1f} MB)")


def save_h5(X: np.ndarray, y: np.ndarray, output_path: str) -> None:
    """Save dataset as an HDF5 file (faster random access during training)."""
    if not H5PY_AVAILABLE:
        raise RuntimeError("h5py not installed. Run: pip install h5py")
    os.makedirs(os.path.dirname(os.path.abspath(output_path)), exist_ok=True)
    with h5py.File(output_path, "w") as f:
        f.create_dataset("X",           data=X,                     compression="gzip")
        f.create_dataset("y",           data=y,                     compression="gzip")
        f.create_dataset("Q_axis",      data=Q_AXIS)
        f.create_dataset("class_names", data=np.array(CLASS_NAMES, dtype="S"))
        f.attrs["source"] = "SimXRD-4M (caobin/SimXRD, Hugging Face)"
        f.attrs["n_bins"] = N_BINS
        f.attrs["Q_min"]  = Q_MIN
        f.attrs["Q_max"]  = Q_MAX
    size_mb = os.path.getsize(output_path) / 1e6
    print(f"[data_loader] Saved → {output_path}  ({size_mb:.1f} MB)")


# ===========================================================================
# SECTION 7  —  Main Pipeline
# ===========================================================================

def run(
    n_per_class:  int  = 5_000,
    output:       str  = "data/processed/simxrd_balanced",
    fmt:          str  = "npz",
    db_dir:       str  = "data/simxrd_shards",
    no_download:  bool = False,
    max_shards:   int  = -1,
) -> None:
    """
    Full end-to-end pipeline:
      1. (Optional) list & download .db shards from Hugging Face.
      2. Stream through shards to collect balanced samples.
      3. Save to disk.

    Parameters
    ----------
    n_per_class  : samples to collect per Bravais class (default 5 000)
    output       : output path (without extension)
    fmt          : 'npz' or 'h5'
    db_dir       : local directory for .db shard files
    no_download  : if True, use already-downloaded files in db_dir
    max_shards   : limit shard downloads (useful for quick tests). -1 = all
    """
    print("=" * 60)
    print("  DeepBravais — SimXRD-4M Data Loader")
    print("=" * 60)
    print(f"  Target : {n_per_class} samples × {N_CLASSES} classes "
          f"= {n_per_class * N_CLASSES:,} total")
    print(f"  Format : {fmt.upper()}")
    print(f"  Output : {output}.{fmt}")
    print("=" * 60 + "\n")

    os.makedirs(db_dir, exist_ok=True)
    db_paths: List[str] = []

    # ── Step 1: Discover / download shards ───────────────────────────────
    if no_download:
        # Use whatever .db files are already on disk
        db_paths = sorted(Path(db_dir).rglob("*.db"))
        if not db_paths:
            sys.exit(
                f"[data_loader] ERROR: --no_download set but no .db files "
                f"found in {db_dir!r}. "
                "Remove --no_download or pre-populate the directory."
            )
        print(f"[data_loader] Found {len(db_paths)} local shard(s) in {db_dir!r}.")
    else:
        print("[data_loader] Querying Hugging Face for shard list …")
        remote_files = _list_db_files()
        if max_shards > 0:
            remote_files = remote_files[:max_shards]
        print(f"[data_loader] Found {len(remote_files)} shard(s). "
              "Downloading (cached if already present) …\n")

        for repo_file in tqdm(remote_files, desc="Downloading shards", unit="shard"):
            local_path = _download_shard(repo_file, local_dir=db_dir)
            db_paths.append(local_path)

        print(f"\n[data_loader] {len(db_paths)} shard(s) ready in {db_dir!r}.\n")

    if not db_paths:
        sys.exit("[data_loader] No .db files available. Aborting.")

    # ── Step 2: Collect balanced samples ─────────────────────────────────
    print("[data_loader] Streaming shards for stratified sampling …\n")
    X, y = collect_balanced_samples(
        db_paths    = [str(p) for p in db_paths],
        n_per_class = n_per_class,
    )

    print(f"\n[data_loader] Final dataset: X={X.shape}  y={y.shape}")

    # ── Step 3: Save ──────────────────────────────────────────────────────
    if fmt == "h5":
        save_h5(X, y, output + ".h5")
    else:
        # savez_compressed adds .npz automatically
        save_npz(X, y, output)

    print("\n[data_loader] Done ✓")
    print(f"  Load with: data = np.load('{output}.{fmt}')")


