# ===========================================================================
# SECTION 2  —  Space-Group → Bravais-Lattice Look-Up Table
# ===========================================================================
# Source: International Tables for Crystallography, Vol. A (standard).
# Every one of the 230 space groups maps to exactly one of the 14 Bravais
# lattice types. The centering letter (P, I, F, A, B, C, R) is baked into
# the Hermann-Mauguin symbol and is what distinguishes e.g. cP from cI.
#
# This table was hand-verified against the ITA and pymatgen's spacegroup
# database. It is the canonical crystallographic classification.

def _build_sg_to_bravais() -> Dict[int, int]:
    """
    Return a dict  {space_group_number (1-230): bravais_label (0-13)}.

    Construction strategy
    ---------------------
    We iterate over ranges of space group numbers that belong to the same
    crystal system, then sub-classify by centering using the well-known
    ITA tables. Where a range contains mixed centerings (e.g. monoclinic
    has both P and C), we list each SG individually.
    """
    sg2bravais: Dict[int, int] = {}

    # ── Triclinic (SG 1-2): always P ──────────────────────────────────────
    for sg in range(1, 3):
        sg2bravais[sg] = 0  # aP

    # ── Monoclinic (SG 3-15) ──────────────────────────────────────────────
    # P centerings: 3,4,6,7,10,11,13,14
    # C (or A/B equivalent, standardised to C) centerings: 5,8,9,12,15
    monoclinic_C = {5, 8, 9, 12, 15}
    for sg in range(3, 16):
        sg2bravais[sg] = 2 if sg in monoclinic_C else 1   # mC or mP

    # ── Orthorhombic (SG 16-74) ───────────────────────────────────────────
    # F: 22, 42, 43, 69, 70
    # I: 23, 24, 44, 45, 46, 71, 72, 73, 74
    # C (or A): 20, 21, 35, 36, 37, 38, 39, 40, 41, 63, 64, 65, 66, 67, 68
    # Everything else in 16-74 → P
    ortho_F = {22, 42, 43, 69, 70}
    ortho_I = {23, 24, 44, 45, 46, 71, 72, 73, 74}
    ortho_C = {20, 21, 35, 36, 37, 38, 39, 40, 41, 63, 64, 65, 66, 67, 68}
    for sg in range(16, 75):
        if sg in ortho_F:
            sg2bravais[sg] = 5   # oF
        elif sg in ortho_I:
            sg2bravais[sg] = 4   # oI
        elif sg in ortho_C:
            sg2bravais[sg] = 6   # oC
        else:
            sg2bravais[sg] = 3   # oP

    # ── Tetragonal (SG 75-142) ────────────────────────────────────────────
    # I (body-centred): 79,80,82,87,88,97,98,107,108,109,110,
    #                   119,120,121,122,139,140,141,142
    # Everything else → P
    tetra_I = {
        79, 80, 82, 87, 88,
        97, 98,
        107, 108, 109, 110,
        119, 120, 121, 122,
        139, 140, 141, 142,
    }
    for sg in range(75, 143):
        sg2bravais[sg] = 8 if sg in tetra_I else 7   # tI or tP

    # ── Trigonal / Rhombohedral (SG 143-167) ──────────────────────────────
    # R centerings (obverse/reverse): 146, 148, 155, 160, 161, 166, 167
    # Everything else → hP
    trigonal_R = {146, 148, 155, 160, 161, 166, 167}
    for sg in range(143, 168):
        sg2bravais[sg] = 9 if sg in trigonal_R else 10   # hR or hP

    # ── Hexagonal (SG 168-194): always P ──────────────────────────────────
    for sg in range(168, 195):
        sg2bravais[sg] = 10   # hP

    # ── Cubic (SG 195-230) ────────────────────────────────────────────────
    # F (face-centred): 196, 202, 203, 209, 210, 216, 217 (wait — 217 is I)
    # Let's enumerate properly from ITA:
    # F: 196, 202, 203, 209, 210, 216, 219, 225, 226, 227, 228
    # I: 197, 199, 204, 206, 211, 214, 217, 220, 229, 230
    # P: everything else in 195-230
    cubic_F = {196, 202, 203, 209, 210, 216, 219, 225, 226, 227, 228}
    cubic_I = {197, 199, 204, 206, 211, 214, 217, 220, 229, 230}
    for sg in range(195, 231):
        if sg in cubic_F:
            sg2bravais[sg] = 13   # cF
        elif sg in cubic_I:
            sg2bravais[sg] = 12   # cI
        else:
            sg2bravais[sg] = 11   # cP

    # Sanity check: all 230 SGs should be assigned
    assert len(sg2bravais) == 230, (
        f"BUG: only {len(sg2bravais)} SGs assigned, expected 230"
    )
    return sg2bravais


# Build once at import time — O(1) lookups from then on
SG_TO_BRAVAIS: Dict[int, int] = _build_sg_to_bravais()


# ===========================================================================
# SECTION 3  —  Signal Pre-processing
# ===========================================================================

# Target length — identical to data_generator.py so both datasets are
# interchangeable without model changes.
N_BINS   = 1024
Q_MIN    = 0.7    # Å⁻¹
Q_MAX    = 6.0    # Å⁻¹

# Fixed Q-axis to interpolate onto
Q_AXIS = np.linspace(Q_MIN, Q_MAX, N_BINS, dtype=np.float32)

# SimXRD stores patterns in d-I format (d = lattice-plane spacing, Å).
# Convert d → Q via  Q = 2π / d  (one-dimensional Bragg condition).

def d_to_Q(d_array: np.ndarray) -> np.ndarray:
    """Convert d-spacing (Å) to momentum transfer Q (Å⁻¹): Q = 2π/d."""
    # Guard against d=0 division
    d_safe = np.where(d_array > 0, d_array, np.inf)
    return (2.0 * np.pi) / d_safe


def preprocess_pattern(
    d_spacings: List[float],
    intensities: List[float],
) -> Optional[np.ndarray]:
    """
    Convert a SimXRD d-I pattern into a 1024-bin Q-space histogram,
    normalised to [0, 1].

    Steps
    -----
    1. Convert d → Q.
    2. Keep only Q values in [Q_MIN, Q_MAX].
    3. Interpolate (scatter) the sparse peak list onto the dense Q_AXIS
       using nearest-neighbour bin assignment (no broadening here — the
       SimXRD patterns already have peak shapes baked in from their own
       simulation).
    4. Normalise to [0, 1].

    Returns None if the pattern is empty or all-zero after processing.
    """
    d_arr = np.asarray(d_spacings,  dtype=np.float64)
    I_arr = np.asarray(intensities, dtype=np.float64)

    if len(d_arr) == 0 or len(I_arr) == 0:
        return None

    # ── Step 1: d → Q ───────────────────────────────────────────────────
    Q_vals = d_to_Q(d_arr)

    # ── Step 2: filter to our Q window ──────────────────────────────────
    mask  = (Q_vals >= Q_MIN) & (Q_vals <= Q_MAX) & np.isfinite(Q_vals)
    Q_vals = Q_vals[mask]
    I_arr  = I_arr[mask]

    if len(Q_vals) == 0:
        return None

    # ── Step 3: bin onto Q_AXIS ──────────────────────────────────────────
    # Find nearest bin index for each peak
    bin_indices = np.searchsorted(Q_AXIS, Q_vals).clip(0, N_BINS - 1)
    signal = np.zeros(N_BINS, dtype=np.float32)
    np.add.at(signal, bin_indices, I_arr.astype(np.float32))

    # ── Step 4: normalise ────────────────────────────────────────────────
    max_val = signal.max()
    if max_val <= 0:
        return None
    signal /= max_val

    return signal


