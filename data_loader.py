# SECTION 5  —  Stratified Sampling Core
# ===========================================================================

def collect_balanced_samples(
    db_paths: List[str],
    n_per_class: int = 5_000,
    verbose: bool = True,
) -> Tuple[np.ndarray, np.ndarray]:
    """
    Iterate over ASE .db shard files and collect exactly `n_per_class`
    samples for each of the 14 Bravais lattices.

    Memory budget
    -------------
    We never load more than one shard into memory at a time.  Each sample
    is a float32 array of length 1024 (4 KB). At n_per_class=5000 and
    14 classes the final array is ~280 MB — well within Kaggle's 16 GB.

    Parameters
    ----------
    db_paths    : list of local paths to SimXRD .db files
    n_per_class : how many samples to collect per Bravais class
    verbose     : print per-class progress

    Returns
    -------
    X : np.ndarray  shape (14 * n_per_class, 1024)  float32
    y : np.ndarray  shape (14 * n_per_class,)       int32
    """
    if not ASE_AVAILABLE:
        raise RuntimeError("ase not installed. Run: pip install ase")

    # Buckets — one list per Bravais class
    buckets: Dict[int, List[np.ndarray]] = {i: [] for i in range(N_CLASSES)}
    counts  = collections.Counter({i: 0 for i in range(N_CLASSES)})
    total_needed = n_per_class * N_CLASSES

    collected = 0   # total samples collected so far

    outer_bar = tqdm(
        db_paths,
        desc  = "Shards",
        unit  = "shard",
        position = 0,
        leave    = True,
    )

    for db_path in outer_bar:
        if collected >= total_needed:
            break   # early exit — all classes full

        db_path = str(db_path)
        if not os.path.isfile(db_path):
            warnings.warn(f"Shard not found, skipping: {db_path}")
            continue

        try:
            db = ase_connect(db_path)
        except Exception as exc:
            warnings.warn(f"Cannot open {db_path}: {exc}")
            continue

        # Count rows in this shard for the inner progress bar
        try:
            n_rows = db.count()
        except Exception:
            n_rows = None

        inner_bar = tqdm(
            db.select(),
            total    = n_rows,
            desc     = f"  └─ {Path(db_path).name}",
            unit     = "row",
            position = 1,
            leave    = False,
        )

        for row in inner_bar:
            if collected >= total_needed:
                break

            # ── Parse tager field ────────────────────────────────────────
            try:
                tager = row.tager
                if isinstance(tager, str):
                    tager = eval(tager)          # stored as stringified list
                space_group = int(tager[0])      # SG number 1-230
            except Exception:
                continue   # skip malformed rows

            # ── Map SG → Bravais label ───────────────────────────────────
            bravais_id = SG_TO_BRAVAIS.get(space_group)
            if bravais_id is None:
                continue   # SG out of range (shouldn't happen)

            # ── Skip if bucket already full ──────────────────────────────
            if counts[bravais_id] >= n_per_class:
                continue

            # ── Parse d-spacing & intensity ──────────────────────────────
            try:
                latt_dis  = row.latt_dis
                intensity = row.intensity
                if isinstance(latt_dis,  str): latt_dis  = eval(latt_dis)
                if isinstance(intensity, str): intensity = eval(intensity)
            except Exception:
                continue

            # ── Pre-process → 1024-bin Q-space pattern ───────────────────
            pattern = preprocess_pattern(latt_dis, intensity)
            if pattern is None:
                continue

            # ── Store ────────────────────────────────────────────────────
            buckets[bravais_id].append(pattern)
            counts[bravais_id] += 1
            collected += 1

            inner_bar.set_postfix(
                collected = collected,
                need      = total_needed,
            )

        inner_bar.close()
        outer_bar.set_postfix(
            collected = collected,
            classes_done = sum(1 for v in counts.values() if v >= n_per_class),
        )

    # ── Report and warn about incomplete classes ─────────────────────────
    print("\n" + "─" * 60)
    print(f"{'Bravais':>10}  {'Label':>5}  {'Collected':>10}  {'Target':>8}")
    print("─" * 60)
    incomplete = []
    for bravais_id in range(N_CLASSES):
        n = counts[bravais_id]
        flag = "✓" if n >= n_per_class else "✗ INCOMPLETE"
        print(f"{CLASS_NAMES[bravais_id]:>10}  {bravais_id:>5}  {n:>10}  "
              f"{n_per_class:>8}  {flag}")
        if n < n_per_class:
            incomplete.append(CLASS_NAMES[bravais_id])
    print("─" * 60)
    if incomplete:
        warnings.warn(
            f"These classes had fewer than {n_per_class} samples in the "
            f"downloaded shards: {incomplete}. "
            "Increase the number of shards or reduce n_per_class.",
            RuntimeWarning,
            stacklevel=2,
        )

    # ── Assemble final arrays ─────────────────────────────────────────────
    X_list, y_list = [], []
    for bravais_id in range(N_CLASSES):
        samples = buckets[bravais_id][:n_per_class]   # cap at n_per_class
        X_list.extend(samples)
        y_list.extend([bravais_id] * len(samples))

    X = np.stack(X_list, axis=0).astype(np.float32)   # (N, 1024)
    y = np.array(y_list,           dtype=np.int32)     # (N,)

    # Shuffle
    perm = np.random.default_rng(seed=42).permutation(len(X))
    X, y = X[perm], y[perm]

    return X, y


